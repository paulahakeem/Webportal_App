class StudentData:
    """Stores the information about a student"""
    def __init__(self, userProfile, firstName, lastName, rollNo, email, cell, year, hometown, dob, githubUname, password, hall):
    	self.userProfile = userProfile;
    	self.firstName = firstName;
    	self.lastName = lastName;
    	self.rollNo = rollNo;
    	self.email = email;
    	self.cell = cell;
    	self.year = year;
    	self.hometown = hometown;
    	self.dob = dob;
    	self.githubUname = githubUname;
    	self.password = password;
    	self.hall = hall;